# Foydalanuvchidan baho kiriting (1 dan 5 gacha). Dastur bahoga qarab quyidagicha xabar chiqarsin:

# Agar baho 5 bo‘lsa → “A’lo”

# Agar baho 4 bo‘lsa → “Yaxshi”

# Agar baho 3 bo‘lsa → “Qoniqarli”

# Agar baho 2 bo‘lsa → “Qoniqarsiz”

# Aks holda → “Noto‘g‘ri baho kiritildi”

# 2-masala

# Foydalanuvchidan yoshini kirtishini so'rang va kiritilgan yoshga qarab quyidagi xabarlarni chiqaring:
# Agar foydalanuvchi 18 yoshdan katta bo'lsa → “Sizga kirish narxi 10000 so'm”
# Agar foydalanuvchi 18 yoshda bo'lsa → “Sizga kirish narxi 5000 so'm”
# Agar foydalanuvchi 18 yoshdan kichik bo'lsa → “Sizga kirish narxi bepul”


# 3-masala
# Foydalanuvchidan son kiritishini sorang va kiritilgan sonning musbat yoki manfiyligini aniqlang.
# Agar son musbat bo'lsa → “Musbat son”
# Agar son manfiy bo'lsa → “Manfiy son”
# Agar foydalanuvchi 0 kiritgan bo'lsa → “Bu nol”
